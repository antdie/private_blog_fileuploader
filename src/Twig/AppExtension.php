//    }

    public function formatBytes($size, $precision = 2)
    {
        $base = log($size, 1024);
        $suffixes = array('B', 'KB', 'MB', 'GB', 'TB', 'PB', 'EB', 'ZB', 'YB');

        return round(pow(1024, $base - floor($base)), $precision) .' '. $suffixes[floor($base)];

    }
}
